seconds=int(input("enter seconds: "))
days=int(seconds/(24*3600))
hours=int((seconds-int(days*24*3600))/3600)
minutes=int((seconds-(days*24*3600+hours*3600))/60)
sec=seconds-(days*24*3600+hours*3600+minutes*60)
print ("days: ",days)
print ("hours: ",hours)
print ("minutes: ",minutes)
print ("seconds: ",sec)